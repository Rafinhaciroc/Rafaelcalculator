
<h2>Operadores Relacinais</h2>

<hr>

<p>
    
    Operadores relacionias são utilizados
    para comprar a relação entre "coisas".

</p>

<ul>
    <li> > | Maior</li>
    <li> < | Menor</li>
    <li> >=| Maior Igual</li>
    <li> <=| Maior Igual</li>
    <li> !+| Diferente</li>
    <li> ==| Igual</li>
</ul>

<p>
    
    Os operadores relacionais retornam verdadeiro
    (true),ou falso (false). Em php true pode ser 
    representado por 1 ou por uma variavel com valor 
    e falso pode ser representado por 0 ou pela 
    inexistência de valor em um variável.

</p>

<?php
    $valor = 10 > 10;
    var_dump ($valor);
?>

